import random
import numpy as np

class Maze:

    start = (0 , 0)
    def __init__(self, rows, cols):
        self.rows = rows
        self.cols = cols
        self.maze = np.full((rows, cols), 1)
        end = (rows - 1, cols - 1 )

    def get(self,cell):
        (x, y) = cell.position
        return self.maze[x][y]

    def create_maze(self):
        neighbors = None
        self.maze[self.start] = 0
        self.maze[self.end] = 0
        for row in range(self.rows-1):
            for col in range(self.cols-1):
                if row > 0:
                    neighbors.append((row-1,col))
                if col > 1:
                    neighbors.append((row, col-1))

                if not neighbors:
                    continue

                (toss_coin_x, toss_coin_y) = random.choice(neighbors)
                self.maze[toss_coin_x, toss_coin_y] = 0

